 		<h3>What's the Hospital Value-Based Purchasing (VBP) Program?</h3>
		<p>The Hospital VBP Program rewards acute care hospitals with incentive payments for the quality of care they give to people with Medicare. 
		This program adjusts payments to hospitals under the Inpatient Prospective Payment System (IPPS) based on the quality of care they deliver.</p>
		<h3>Why's the Hospital VBP Program important?</h3>
		<p>The Hospital VBP Program is designed to make:</p>
		<ul>
			<li>The quality of care better for hospital patients.</li>		
			<li>Hospital stays a better experience for patients.</li>	
		</ul>
		<p>The Hospital VBP Program encourages hospitals to improve the quality and safety of acute inpatient care for Medicare beneficiaries and all patients by:</p>
		<ul>
			<li>Eliminating or reducing adverse events (healthcare errors resulting in patient harm).</li>	
			<li>Adopting evidence-based care standards and protocols that make the best outcomes for the most patients.	</li>
			<li>Changing hospital processes to make patientsâ€™ care experiences better.</li>
			<li>Increasing care transparency for consumers.</li>
			<li>Recognizing hospitals that give high-quality care at a lower cost to Medicare.</li>
		</ul>
		<h3>How does the program work?</h3>
		<p>We pay participating hospitals for inpatient acute care services based on the quality of care they provide, not the quantity of services they provide, in a given performance period.</p>
		<p>The program:</p>
		<ul>
			<li>Withholds participating hospitals&rsquo; Medicare payments by a percentage specified by law.</li>
			<li>Uses the estimated total amount of those reductions to fund value-based incentive payments to hospitals based on their performance in the program.</li>
			<li>Applies the net result of the reduction and the incentive as a claim-by-claim adjustment factor to the base operating Medicare severity <a href="https://www.cms.gov/Medicare/Medicare-Fee-for-Service-Payment/AcuteInpatientPPS/index.html">diagnosis-related group </a>(MS-DRG) payment&nbsp;amount for Medicare fee-for-service claims in the fiscal year associated with the performance period.</li>
		</ul>
		<h3>What measures are used in the Hospital VBP Program?</h3>
		<p>Hospitals are gauged on measures of outcome such as:</p>
		<ul>
			<li>Mortality and complications</li>
			<li>Healthcare-associated infections</li>
			<li>Patient safety</li>
			<li>Patient experience</li>
			<li>Process</li>
			<li>Efficiency and cost reduction</li>
		</ul>
		<p>Each hospital may earn 2 scores on each measure&mdash;one for achievement and one for improvement. The final score awarded to a hospital for each measure is the higher of these 2 scores. We adjust a part of hospitals&rsquo; Medicare payments based on a total performance score that reflects, on a measure-by-measure basis:</p>
		<ul>
			<li>How well they perform compared to all hospitals, or</li>
			<li>How much they improve their own performance compared to their performance during a prior baseline period.</li>
		</ul>
